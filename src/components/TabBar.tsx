"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const tabs = [
  { href: "/dashboard", label: "Plan", icon: MapIcon },
  { href: "/dashboard/feed", label: "Feed", icon: FeedIcon },
  { href: "/dashboard/community", label: "Community", icon: GroupIcon },
  { href: "/dashboard/profile", label: "Profile", icon: ProfileIcon },
];

export default function TabBar() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 z-50">
      <div className="flex justify-around items-center h-16 max-w-lg mx-auto">
        {tabs.map((tab) => {
          const active =
            tab.href === "/dashboard"
              ? pathname === "/dashboard"
              : pathname.startsWith(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`flex flex-col items-center gap-0.5 text-xs transition-colors ${active ? "text-black" : "text-gray-400"}`}
            >
              <tab.icon active={active} />
              <span className="font-medium">{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

